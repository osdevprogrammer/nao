static inline void draw_char(uint32_t* fb, int x, int y, char c, uint32_t color, uint32_t pitch, void (*putpixel)(uint32_t*, int, int, uint32_t, uint32_t)) {
    // Basic safety bound check for valid printable ASCII index region
    if ((uint8_t)c > 127) return;

    for (int row = 0; row < 8; row++) {
        uint8_t row_data = font_bitmap[(uint8_t)c][row];
        for (int col = 0; col < 8; col++) {
            if (row_data & (0x80 >> col)) {
                putpixel(fb, x + col, y + row, color, pitch);
            }
        }
    }
}

